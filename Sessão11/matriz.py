#Listas Bidimensionais --> Matrizes

matriz = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]
print (matriz)
matriz = [[1, 2, 3],[4, 5, 6],[7, 8, 9]] # pode ser também assim designada
print (matriz)


print (matriz[0]) # --> devolve o 1º indice da matriz [1, 2, 3]
print (matriz[1]) # --> devolve o 2º indice da matriz [4, 5, 6]
print(matriz[2])
print (matriz[1][0])  # --> devolve o 1º valor do 2º indice da matriz [4, 5, 6]
print (matriz[0][1])  # --> devolve o 2º valor do 1º indice da matriz [4, 5, 6]
print(matriz[2][2])
